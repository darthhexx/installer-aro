package bootkube

import (
	"os"
	"path/filepath"

	"github.com/openshift/installer/pkg/asset"
	"github.com/openshift/installer/pkg/asset/templates/content"
)

const (
	openshiftAPIServerNamespaceFileName = "openshift-apiserver-namespace.yaml"
)

var _ asset.WritableAsset = (*OpenshiftAPIServerNamespace)(nil)

// OpenshiftAPIServerNamespace is an asset for the openshift-apiserver namespace
type OpenshiftAPIServerNamespace struct {
	FileList []*asset.File
}

// Dependencies returns all of the dependencies directly needed by the asset
func (t *OpenshiftAPIServerNamespace) Dependencies() []asset.Asset {
	return []asset.Asset{}
}

// Name returns the human-friendly name of the asset.
func (t *OpenshiftAPIServerNamespace) Name() string {
	return "OpenshiftAPIServerNamespace"
}

// Generate generates the actual files by this asset
func (t *OpenshiftAPIServerNamespace) Generate(parents asset.Parents) error {
	fileName := openshiftAPIServerNamespaceFileName
	data, err := content.GetBootkubeTemplate(fileName)
	if err != nil {
		return err
	}
	t.FileList = []*asset.File{
		{
			Filename: filepath.Join(content.TemplateDir, fileName),
			Data:     []byte(data),
		},
	}
	return nil
}

// Files returns the files generated by the asset.
func (t *OpenshiftAPIServerNamespace) Files() []*asset.File {
	return t.FileList
}

// Load returns the asset from disk.
func (t *OpenshiftAPIServerNamespace) Load(f asset.FileFetcher) (bool, error) {
	file, err := f.FetchByName(filepath.Join(content.TemplateDir, openshiftAPIServerNamespaceFileName))
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}
	t.FileList = []*asset.File{file}
	return true, nil
}
